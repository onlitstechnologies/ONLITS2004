#include<stdio.h>

int main()
{
	float bs, da, hra, gs;
	
	printf("Enter Ramesh's basic salary: ");
	scanf("%f", &bs);
	
	da = bs * 40/100;
	hra = bs * 20/100;
	gs = bs + da + hra;
	
	printf("The gross salary of Ramesh is %1.2f.\n", gs);
	
	return 0;
}

